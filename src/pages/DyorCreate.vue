<script>
import axios from "axios";
import myUpload from "vue-image-crop-upload";
const columns = ["logo", "name", "token"];

const sorted = {};
columns.forEach(function (key) {
  sorted[key] = 1;
});

export default {
  async mounted() {
    this.inputScanner();
  },
  components: {
    "my-upload": myUpload,
  },
  data() {
    return {
      errors: {
        pn: false,
        ps: false,
        pd: false,
        pc: false,
        pw: false,
        pr: false,
        an: false,
      },
      show: false,
      params: {
        token: "123456798",
        name: "avatar",
      },
      langType: "en",
      headers: {
        smail: "*_~",
      },
      imgDataUrl: "",
      tableData: [],
      columns: columns,
      sortKey: "",
      pn: "",
      ps: "",
      pd: "",
      pc: "",
      pw: "",
      pr: "",
      an: "",
      pl: " ",
      isDeployed: false,
      isSelected: false,
      isUploaded: false,
    };
  },
  watch: {
    pn() {
      if (this.pn.length <= 0 && this.isSelected) {
        this.isDeployed = false;
        this.isSelected = false;
        this.resetInputs();
      }
      if (!this.pn.length <= 0) {
        this.isDeployed = true;
      }
      if (this.isSelected) {
        this.isDeployed = false;
      }
    },
  },
  methods: {
    resetInputs() {
      this.ps = "";
      this.pl = "";
    },
    toggleShow() {
      this.show = !this.show;
    },
    cropSuccess(imgDataUrl, field) {
      this.pl = imgDataUrl;
      this.isUploaded = true;
      console.log(imgDataUrl, field);
    },

    cropUploadSuccess(jsonData, field) {
      console.log("-------- upload success --------");
      console.log(jsonData);
      console.log("field: " + field);
    },
    cropUploadFail(status, field) {
      console.log("-------- upload fail --------");
      this.pl = "";
      console.log(status);
      console.log("field: " + field);
    },
    async inputScanner() {
      let timer;

      const waitTime = 500;

      const input = document.querySelector("#tableTradeSearch");

      input.addEventListener("keyup", () => {
        clearTimeout(timer);
        timer = setTimeout(() => {
          this.searchCoins();
        }, waitTime);
      });
    },
    capitalize(str) {
      return str.charAt(0).toUpperCase() + str.slice(1);
    },
    sortBy(key) {
      this.sortKey = key;
      this.sortOrders[key] = this.sortOrders[key] * -1;
    },
    configProject(element) {
      this.pl = element.image;
      this.pn = element.name;
      this.ps = element.token;
      this.isSelected = true;
    },
    async updateCoins() {
      if (this.pn !== "") {
        try {
          const res = await axios.get(
            `https://api.coingecko.com/api/v3/search?query=${this.pn}`
          );
          this.tableData = res.data.coins.map((e) => {
            return {
              id: e.id,
              logo: e.thumb,
              name: e.name,
              token: e.symbol,
              image: e.large,
            };
          });
        } catch (error) {
          console.log(error);
        }
      }
    },
    deployDropdown() {
      this.isDeployed = !this.isDeployed;
    },
    nextResearch() {
      if (this.checkData()) {
        this.$store.commit("modifyAuditData", {
          pn: this.pn.toLowerCase(),
          ps: this.ps.toLowerCase(),
          pd: this.pd.toLowerCase(),
          pc: this.pc.toLowerCase(),
          pw: this.pw.toLowerCase(),
          pr: this.pr.toLowerCase(),
          an: this.an.toLowerCase(),
          pl: this.pl,
        });
        this.$router.push("/research");
      }
    },
    checkData() {
      (this.errors = {
        pn: false,
        ps: false,
        pd: false,
        pc: false,
        pw: false,
        pr: false,
        an: false,
        pl: false,
      }),
        this.pn.length > 0 ? true : (this.errors.pn = true);
      this.pn.length < 50 ? true : (this.errors.pn = true);

      this.ps.length > 0 ? true : (this.errors.ps = true);
      this.ps.length < 20 ? true : (this.errors.ps = true);

      this.pd.length > 0 ? true : (this.errors.pd = true);
      this.pd.length < 200 ? true : (this.errors.pd = true);

      this.pc.length > 0 ? true : (this.errors.pc = true);
      this.pc.length < 100 ? true : (this.errors.pc = true);

      this.pw.length > 0 ? true : (this.errors.pw = true);
      this.pw.length < 50 ? true : (this.errors.pw = true);

      this.pr.length > 0 ? true : (this.errors.pr = true);
      this.pr.length < 100 ? true : (this.errors.pr = true);

      this.an.length > 0 ? true : (this.errors.an = true);
      this.an.length < 16 ? true : (this.errors.an = true);

      this.pl.length > 0 ? true : (this.errors.pl = true);

      console.log(Object.values(this.errors));

      return !Object.values(this.errors).includes(true) ? true : false;
    },
    async searchCoins() {
      this.updateCoins();
    },
  },
  computed: {
    filteredCoins() {
      const sortKey = this.sortKey;
      const filterKey = this.pn && this.pn.toLowerCase();
      const order = this.sortOrders[sortKey] || 1;
      let tableData = this.tableData;
      if (filterKey) {
        tableData = tableData.filter(function (row) {
          return Object.keys(row).some(function (key) {
            return String(row[key]).toLowerCase().indexOf(filterKey) > -1;
          });
        });
      }
      if (sortKey) {
        tableData = tableData.slice().sort(function (a, b) {
          a = a[sortKey];
          b = b[sortKey];
          return (a === b ? 0 : a > b ? 1 : -1) * order;
        });
      }
      return tableData;
    },
    sortOrders() {
      const columnSortOrders = {};

      this.columns.forEach(function (key) {
        columnSortOrders[key] = 1;
      });

      return columnSortOrders;
    },
  },
};
</script>

